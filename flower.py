import turtle
turtle.pensize(2)
turtle.speed(20)
turtle.bgcolor("lightskyblue")
turtle.shape("turtle")
for i in range(36):
    turtle.pencolor("crimson")
    turtle.circle(100,60)
    turtle.left(120)
    turtle.circle(100,60)
    turtle.left(120)
    turtle.left(10)
turtle.pencolor("gold")
turtle.goto(0,-20)
turtle.fillcolor("gold")
turtle.begin_fill()
turtle.circle(20)
turtle.end_fill()
turtle.right(90)
turtle.pencolor("green")
turtle.forward(160)
turtle.backward(60)
turtle.fillcolor("green")
turtle.begin_fill()
turtle.right(180)
turtle.circle(100,50)
turtle.left(130)
turtle.circle(100,50)
turtle.end_fill()
turtle.right(50)
turtle.forward(60)
turtle.mainloop()
